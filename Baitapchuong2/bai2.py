# Bài 2: Nhập chuỗi ký tự từ bàn phím và in lại chuỗi đó.

def main() -> None:
	chuoi_1 = input("Nhập chuỗi ký tự thứ nhất: ").strip()
	chuoi_2 = input("Nhập chuỗi ký tự thứ hai: ").strip()

	print("Kết quả:")
	print(f"- Chuỗi thứ nhất: {chuoi_1}")
	print(f"- Chuỗi thứ hai: {chuoi_2}")


if __name__ == "__main__":
	main()
